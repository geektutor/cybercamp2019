            <h2>Summary</h2>
            <p>This course introduces students with no knowledge of coding or game development how games work under the hood. On the first day (introductory day), the students will gain the basics of game design and discover how to make a game with no prior experience at all. The students will also learn how to use the digital creation tool ready.io to create their very first game. At the end of the introductory day, the students who decide to specialise in game design and development will then get exposed to the tools professionals use in the real world to make video games.</p>
            <p>The specialisation course will introduce students to industry standard tools used by game developers. For a period of 5 days, students will develop game development skills and enhance their creative side through a series of exercises using the Unity Playground package. During the 5 day specialisation, the students will practise and demonstrate their knowledge of a full game production cycle which includes conceptualisation, design, implementation and testing. By the end of specialisation,each student will have learnt how to design and develop their own video games by using the same tools the pros use.</p>
            <article>
              <b>What the students gain through this course:</b>
              <ul>
                <li>Recognise simple game design concepts</li>
                <li>Enjoy using simple drag and drop logic/programming to create game mechanics</li>
                <li>Practise fundamental game design principles</li>
                <li>Develop essential game design skills and creativity</li>
                <li>Learn to build their own games from start to finish</li>
              </ul>
            </article>
            <article>
              <b>Technical Takeaways:</b>
              <ul>
                <li>Physics and Classical Mechanics (Rigidbody, Velocity, Drag, Collisions etc.)</li>
                <li>Character Composition and Animation</li>
                <li>Loops, Patterns and Events</li>
                <li>Conditional Statements and Logic</li>
                <li>Variables, Behaviors and Math</li>
                <li>Game Mechanics and Gameplay</li>
                <li>Game Genres (Tower Defense, Endless, Puzzle)</li>
                <li>Non Playable Characters (NPCs)</li>
                <li>Repetitions, Delays, and Randomness</li>
                <li>Audio Implementation and Design</li>
                <li>User Interfaces</li>
                <li>Player Input Detection and Interaction</li>
                <li>Iterative Development</li>
              </ul>
